package org.blog.domain;

public class BlogVO {

	private int blog_no;
	private String blog_user;
	private String blog_img;
	private String profile_img;
	private String blog_title;
	private String blog_info;
	private String profile_info;
	
	
	
	public int getBlog_no() {
		return blog_no;
	}
	public void setBlog_no(int blog_no) {
		this.blog_no = blog_no;
	}
	public String getBlog_user() {
		return blog_user;
	}
	public void setBlog_user(String blog_user) {
		this.blog_user = blog_user;
	}
	public String getBlog_img() {
		return blog_img;
	}
	public void setBlog_img(String blog_img) {
		this.blog_img = blog_img;
	}
	public String getProfile_img() {
		return profile_img;
	}
	public void setProfile_img(String profile_img) {
		this.profile_img = profile_img;
	}
	public String getBlog_title() {
		return blog_title;
	}
	public void setBlog_title(String blog_title) {
		this.blog_title = blog_title;
	}
	public String getBlog_info() {
		return blog_info;
	}
	public void setBlog_info(String blog_info) {
		this.blog_info = blog_info;
	}
	public String getProfile_info() {
		return profile_info;
	}
	public void setProfile_info(String profile_info) {
		this.profile_info = profile_info;
	}
	
	
	
	
}
