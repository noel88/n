package org.blog.domain;

public class LikeVO {

	
	private int like_no;
	private int post_no;
	private String like_user;
	private String like_now;
	
	
	
	public int getLike_no() {
		return like_no;
	}
	public void setLike_no(int like_no) {
		this.like_no = like_no;
	}
	public int getPost_no() {
		return post_no;
	}
	public void setPost_no(int post_no) {
		this.post_no = post_no;
	}
	public String getLike_user() {
		return like_user;
	}
	public void setLike_user(String like_user) {
		this.like_user = like_user;
	}
	public String getLike_now() {
		return like_now;
	}
	public void setLike_now(String like_now) {
		this.like_now = like_now;
	}
	
	
	
	
}
