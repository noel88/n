package org.blog.domain;

public class SearchVO {


	private String search;
	private int title_count;
	private int context_count;


	public String getSearch() {
		return search;
	}
	public void setSearch(String search) {
		this.search = search;
	}
	public int getTitle_count() {
		return title_count;
	}
	public void setTitle_count(int title_count) {
		this.title_count = title_count;
	}
	public int getContext_count() {
		return context_count;
	}
	public void setContext_count(int context_count) {
		this.context_count = context_count;
	}




}
