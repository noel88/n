package com.ntoday.domain;

public class MemoVO {

	private int memoNo;
	private String MemoTitle;
	private String MemoText;
	private String MemoNow;


	public int getMemoNo() {
		return memoNo;
	}
	public void setMemoNo(int memoNo) {
		this.memoNo = memoNo;
	}
	public String getMemoTitle() {
		return MemoTitle;
	}
	public void setMemoTitle(String memoTitle) {
		MemoTitle = memoTitle;
	}
	public String getMemoText() {
		return MemoText;
	}
	public void setMemoText(String memoText) {
		MemoText = memoText;
	}
	public String getMemoNow() {
		return MemoNow;
	}
	public void setMemoNow(String memoNow) {
		MemoNow = memoNow;
	}






}
